public class Raices {
    private Float a ;
    private Float b;
    private Float c;
    // Creamos los constructores
    public Raices() {
    }

    public Raices(Float a, Float b, Float c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
// getter and setter
    public Float getA() {
        return a;
    }

    public void setA(Float a) {
        this.a = a;
    }

    public Float getB() {
        return b;
    }

    public void setB(Float b) {
        this.b = b;
    }

    public Float getC() {
        return c;
    }

    public void setC(Float c) {
        this.c = c;
    }

   
    
    
    
}
